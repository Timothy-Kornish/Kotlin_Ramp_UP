package numbers

fun main(args: Array<String>) {
    // val is constant, var is mutable
    val i: Int = 1000
    val d: Double = 100.00
    val f: Float = 100.00f
    val l: Long = 100000004
    val s: Short = 10
    val b: Byte = 1

    println("Int: " + i)
    println("Double: " + d)
    println("Float: " + f)
    println("Long: " + l)
    println("Short: " + s)
    println("Byte: " + b)
}
